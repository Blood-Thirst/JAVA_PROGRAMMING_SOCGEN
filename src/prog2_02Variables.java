public class prog2_02Variables {
    public static void main(String[] args){
        //Declaring a variable
        // the variable should always be declared after decalaring the data type only


        int age = 25;
        double salary = 50000.00 ;
        String name = "Bob Arin";


        //Displaying the variable names


        System.out.println("Name : "+name);
        System.out.println("Age :" + age);
        System.out.println("Salary : " + salary);


        System.out.println("^_^ hello "+name+" glad to meet you. Good to know your salary is "+salary);


        // Update variable value
        age = 26;
        System.out.println("Updated Age: " + age);
    }

}
